package com.neoteric.java.interf;

public class InterfaceJava {
    public void  numbering(int i){
        if (i==1){
            System.out.println("add");
        }else  if (i==2){
            System.out.println("sub");
        }else if (i==3){
            System.out.println("division");
        }
        else{
            System.out.println("unknown input");
        }
    }
}
